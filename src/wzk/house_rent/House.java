package wzk.house_rent;

public class House {
    private int id;
    private String name;
    private String address;
    private String telephone;
    private String status;

    public House(int id, String name, String address, String telephone, String status) {
        this.id = id;
        this.name = name;
        this.address = address;
        this.telephone = telephone;
        this.status = status;
    }

}
